package com.batodev.arrows.data

import kotlinx.coroutines.flow.Flow

@Suppress("TooManyFunctions")
open class UserPreferencesRepository(private val dao: UserPreferencesDao) {

    open val theme: Flow<String> get() = dao.getTheme()
    open val animationSpeed: Flow<String> get() = dao.getAnimationSpeed()
    open val isVibrationEnabled: Flow<Boolean> get() = dao.getIsVibrationEnabled()
    open val isSoundsEnabled: Flow<Boolean> get() = dao.getIsSoundsEnabled()
    open val isFillBoardEnabled: Flow<Boolean> get() = dao.getIsFillBoardEnabled()
    open val levelNumber: Flow<Int> get() = dao.getLevelNumber()
    open val currentLives: Flow<Int> get() = dao.getCurrentLives()
    open val debugForcedWidth: Flow<Int?> get() = dao.getDebugForcedWidth()
    open val debugForcedHeight: Flow<Int?> get() = dao.getDebugForcedHeight()
    open val debugForcedLives: Flow<Int?> get() = dao.getDebugForcedLives()
    open val debugForcedShape: Flow<String?> get() = dao.getDebugForcedShape()
    open val initialLevel: Flow<String?> get() = dao.getInitialLevel()
    open val currentLevel: Flow<String?> get() = dao.getCurrentLevel()
    open val isAdFree: Flow<Boolean> get() = dao.getIsAdFree()
    open val rewardAdCount: Flow<Int> get() = dao.getRewardAdCount()
    open val gamesCompleted: Flow<Int> get() = dao.getGamesCompleted()
    open val introCompleted: Flow<Boolean> get() = dao.getIntroCompleted()
    open val isWinVideosEnabled: Flow<Boolean> get() = dao.getIsWinVideosEnabled()

    open suspend fun saveThemePreference(theme: String) = dao.updateTheme(theme)
    open suspend fun saveAnimationSpeed(speed: String) = dao.updateAnimationSpeed(speed)
    open suspend fun saveVibrationPreference(enabled: Boolean) = dao.updateVibrationEnabled(enabled)
    open suspend fun saveSoundsPreference(enabled: Boolean) = dao.updateSoundsEnabled(enabled)
    open suspend fun saveFillBoardPreference(enabled: Boolean) = dao.updateFillBoardEnabled(enabled)
    open suspend fun saveLevelNumber(level: Int) = dao.updateLevelNumber(level)
    open suspend fun saveCurrentLives(lives: Int) = dao.updateCurrentLives(lives)
    open suspend fun saveDebugForcedWidth(width: Int?) = dao.updateDebugForcedWidth(width)
    open suspend fun saveDebugForcedHeight(height: Int?) = dao.updateDebugForcedHeight(height)
    open suspend fun saveDebugForcedLives(lives: Int?) = dao.updateDebugForcedLives(lives)
    open suspend fun saveDebugForcedShape(shape: String?) = dao.updateDebugForcedShape(shape)
    open suspend fun saveInitialLevel(levelJson: String) = dao.updateInitialLevel(levelJson)
    open suspend fun saveCurrentLevel(levelJson: String) = dao.updateCurrentLevel(levelJson)
    open suspend fun clearSavedLevel() = dao.clearSavedLevel()
    open suspend fun saveIsAdFree(isAdFree: Boolean) = dao.updateIsAdFree(isAdFree)
    open suspend fun incrementRewardAdCount() = dao.incrementRewardAdCount()
    open suspend fun resetRewardAdCount() = dao.resetRewardAdCount()
    open suspend fun saveIntroCompleted(completed: Boolean) = dao.updateIntroCompleted(completed)
    open suspend fun saveWinVideosEnabled(enabled: Boolean) = dao.updateWinVideosEnabled(enabled)
    open suspend fun incrementGamesCompleted() = dao.incrementGamesCompleted()
}
